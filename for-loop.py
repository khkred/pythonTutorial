# We want to capitalise the city names

cities = ['new york city', 'mountain view', 'chicago', 'los angeles']

capitalised_cities = []

# in this for loop we are capitalising every city
# and then we area adding it to the capitalised_cities list

for city in cities:

	capitalised_cities.append(city.title())

# Now we will just print out the capitalised cities

for city in capitalised_cities:
	print(city)















